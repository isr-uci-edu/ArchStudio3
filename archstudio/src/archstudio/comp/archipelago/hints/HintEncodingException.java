package archstudio.comp.archipelago.hints;

public class HintEncodingException extends Exception {

	public HintEncodingException() {
		super();
	}

	public HintEncodingException(String arg0) {
		super(arg0);
	}

	public HintEncodingException(Throwable arg0) {
		super(arg0);
	}

	public HintEncodingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
