package archstudio.comp.archipelago.hints;

public class HintDecodingException extends Exception {

	public HintDecodingException() {
		super();
	}

	public HintDecodingException(String arg0) {
		super(arg0);
	}

	public HintDecodingException(Throwable arg0) {
		super(arg0);
	}

	public HintDecodingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
